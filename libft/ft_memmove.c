#include "libft.h"

void	*ft_memmove(void *dest, const void *src, unsigned int n)
{
	unsigned int	i;
	char			*p_dest;
	char			*p_src;

	i = 0;
	p_dest = (char *)dest;
	p_src = (char *)src;
	if (p_src < p_dest)
		while (n-- > 0)
			p_dest[n] = p_src[n];
	else
	{
		while (i < n)
		{
			p_dest[i] = p_src[i];
			i++;
		}
	}
	return (dest);
}
